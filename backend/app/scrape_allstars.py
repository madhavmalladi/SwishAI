import sqlite3

# Database file path
DB_PATH = "nba_players.db"

def create_database():
    """Create the database and players table if they don't exist"""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    cursor.execute("SELECT name FROM sqlite_master WHERE type='table' AND name='players'")
    table_exists = cursor.fetchone() is not None
    if not table_exists:
        cursor.execute('''
            CREATE TABLE players (
                id INTEGER PRIMARY KEY,
                name TEXT NOT NULL,
                all_star_count INTEGER,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        ''')
    else:
        cursor.execute("PRAGMA table_info(players)")
        columns = [column[1] for column in cursor.fetchall()]
        if 'all_star_count' not in columns:
            cursor.execute("ALTER TABLE players ADD COLUMN all_star_count INTEGER")
            print("‚úÖ Added all_star_count column to existing table")
    conn.commit()
    conn.close()

def save_players_to_db(player_pool):
    """Save players to the database"""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    cursor.execute("DELETE FROM players")
    for player in player_pool:
        cursor.execute("INSERT INTO players (id, name, all_star_count) VALUES (?, ?, ?)",
                      (player["id"], player["name"], player["all_star_count"]))
    conn.commit()
    conn.close()
    print(f"Saved {len(player_pool)} All-Star players to database")

